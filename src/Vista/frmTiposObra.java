    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        if (crud.insert(nombre.getText())) {
            limpiar("Registro creado correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "Error al crear Registro");
        }
    }//GEN-LAST:event_guardarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        if (crud.delete(codigo)) {
            limpiar("Registro eliminado correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar Registro");
        }
        cancelar.setEnabled(false);
        modificar.setEnabled(false);
        eliminar.setEnabled(false);
        guardar.setEnabled(true);
    }//GEN-LAST:event_eliminarActionPerformed

    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        if (crud.update(codigo, nombre.getText())) {
            limpiar("Registro modificado correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "Error al modificar Registro");
        }
        cancelar.setEnabled(false);
        modificar.setEnabled(false);
        eliminar.setEnabled(false);
        guardar.setEnabled(true);
    }//GEN-LAST:event_modificarActionPerformed

