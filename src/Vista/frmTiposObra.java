    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        //Seleccionar el dato al hacer click con el mouse
        fila = jTable2.rowAtPoint(evt.getPoint());
        if (fila > -1) {
            codigo = Integer.parseInt(String.valueOf(jTable2.getValueAt(fila, 0)));
            nombre.setText(String.valueOf(jTable2.getValueAt(fila, 1)));
            //activar-desactivar botones
            cancelar.setEnabled(true);
            modificar.setEnabled(true);
            eliminar.setEnabled(true);
            guardar.setEnabled(false);
        }
    }//GEN-LAST:event_jTable2MouseClicked


