    private void llenar_tabla() {
        ArrayList<Administrador> datos = crud.read();
        //Objeto de tipo Tabla
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.setColumnCount(0);
        tabla.addColumn("Nombre");
        tabla.addColumn("Email Usuario");
        tabla.addColumn("Password Usuario");
        tabla.addColumn("Email Admin");
        tabla.addColumn("Password Admin");
        tabla.setNumRows(datos.size());
         Administrador a = new Administrador();
        for (int i = 0; i < datos.size(); i++) {
            a = (Administrador) datos.get(i);
            tabla.setValueAt(a.getNombreUsuario(), i, 0);
            tabla.setValueAt(a.getPasswordUsuario(), i, 1);
            tabla.setValueAt(a.getEmailUsuario(), i, 2);
            tabla.setValueAt(a.getEmAdmin(), i, 3);
            tabla.setValueAt(a.getPwAdmin(), i, 4);
        }
        jTable1.setModel(tabla);
    }
    
